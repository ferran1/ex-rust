fn main (){

    let x= 10;

    if x == 10 {
        println!("The variable is 10");
    } else if x == 20 {
        println!("The variable is 20")
    } else {
        println!("The variable isn't either 10 or 20..")
    }

    // If in a let statement
    if x != 10 {
        5  // If x is unequal to 10, x should be 5
    } else {
        15 // Otherwise x should be 15
    };
}
